package com.model;

public class DropDown {
	int trn_no;
	String trn_name;
	String trn_start;
	String trn_end;

	public DropDown() {

	}

	public int getTrn_no() {
		return trn_no;
	}

	public void setTrn_no(int trn_no) {
		this.trn_no = trn_no;
	}

	public String getTrn_name() {
		return trn_name;
	}

	public void setTrn_name(String trn_name) {
		this.trn_name = trn_name;
	}

	public String getTrn_start() {
		return trn_start;
	}

	public void setTrn_start(String trn_start) {
		this.trn_start = trn_start;
	}

	public String getTrn_end() {
		return trn_end;
	}

	public void setTrn_end(String trn_end) {
		this.trn_end = trn_end;
	}

}
